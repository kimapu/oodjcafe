    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //addnew record
        Drinks newDrinks = new Drinks();
        boolean unFilled = Validator.empty(
                jTextField1.getText(),
                jTextField2.getText(),
                jTextField3.getText(),
                jTextField4.getText()
        );
        if ( unFilled ) {
            JOptionPane.showMessageDialog(rootPane, "Some input fields seem missing!");
            clear();
            jTextField1.grabFocus();
        } else {
            newDrinks.setId(Integer.parseInt(jTextField1.getText()));//convert from String into Integer
            newDrinks.setName(jTextField2.getText());
            newDrinks.setQuantity(jTextField3.getText());
            newDrinks.setSupplier(jTextField4.getText());

            admin.addDrinks(newDrinks);
            JOptionPane.showMessageDialog(rootPane, "Added.");
            fill(newDrinks);
            clear();
            
            model.addRow(new String[]{
                Integer.toString(newDrinks.getId()),
                newDrinks.getName(),
                newDrinks.getQuantity(),
                newDrinks.getSupplier()
            });
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void fill(Drinks drk) {
        jTextField1.setText(Integer.toString(drk.getId()));
        jTextField2.setText(drk.getName());
        jTextField3.setText(drk.getQuantity());
        jTextField4.setText(drk.getSupplier());
    }

    private void clear() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }

    private void updateTable( List<Drinks> data ) {
        //populate the file content into the JTable
        for (Drinks drk : data) {
            model.addRow(new String[]{
                Integer.toString(drk.getId()),
                drk.getName(),
                drk.getQuantity(),
                drk.getSupplier()
            });
        }
    }

