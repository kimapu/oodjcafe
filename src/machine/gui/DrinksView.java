    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        System.out.println("Row: " + row);
        //String id = (String)model.getValueAt(row, 0);
        int id = Integer.parseInt((String) model.getValueAt(row, 0));
        String name = (String) model.getValueAt(row, 1);
        String qty = (String) model.getValueAt(row, 2);
        String supplier = (String) model.getValueAt(row, 3);
        Drinks drk = new Drinks();
//        drk.setId(Integer.parseInt(id));
        drk.setId(id);
        drk.setName(name);
        drk.setQuantity(qty);
        drk.setSupplier(supplier);
        //set selected row data into the textfields
        fill(drk);
        jButton2.setEnabled(false);
        jButton1.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Drinks newDrinks = new Drinks();

        newDrinks.setId(Integer.parseInt(jTextField1.getText()));//convert from String into Integer
        newDrinks.setName(jTextField2.getText());
        newDrinks.setQuantity(jTextField3.getText());
        newDrinks.setSupplier(jTextField4.getText());

        admin.modifyDrinks(newDrinks);
        JOptionPane.showMessageDialog(rootPane, "Updated.");
        model.setRowCount(0);
        updateTable( DrinksAdapter.getDrinks() );
    }//GEN-LAST:event_jButton1ActionPerformed

